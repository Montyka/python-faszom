
# logikai operatorok

# and or not -  &&  || ! ez java vagy c++

szam1 = 2
szam2 = 4

#           True             True
print(szam1 < szam2 and szam1 == 2)

print(szam1 < szam2 and szam1 == 3)

#           True            Not false = true
print(szam1 < szam2 and not szam1 == 3)

# not true = false